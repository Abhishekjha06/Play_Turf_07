    const [currentVerifyStep, setCurrentVerifyStep] = useState(0);
    const [countdownSeconds, setCountdownSeconds] = useState(900); // 15 mins
    const [isCancelling, setIsCancelling] = useState(false);
    const [showTicket, setShowTicket] = useState(false);
    const { user } = useAuth();
    const { ticketRef, downloadPDF, shareTicket, isGenerating } = useBookingTicket();

    // Compute duration & slot hours
    const durationHours = Math.max(1, Math.round(total / turf.price_per_hour));
    const [sh, sm] = booking.start_time.split(":").map(Number);
    const eh = sh + durationHours;
    const end_time_str = `${eh.toString().padStart(2, "0")}:${sm.toString().padStart(2, "0")}`;

    const releasedSlots = [];
    for (let i = 0; i < durationHours; i++) {
        const slotHour = (sh + i).toString().padStart(2, "0");
        releasedSlots.push(formatSlotTime(`${slotHour}:${sm.toString().padStart(2, "0")}`));
    }

    // Step-by-step verification progress simulator
    const verificationSteps = [
        "Payment successfully received",
        "Payment amount matches booking amount",
        "Transaction ID is valid",
        "All requested slots are still available",
        "Slot reservation saved in database"
    ];

    useEffect(() => {
        if (verificationState === "verifying") {
            const interval = setInterval(() => {
                setCurrentVerifyStep((prev) => {
                    if (prev >= verificationSteps.length - 1) {
                        clearInterval(interval);
                        setTimeout(() => {
                            setVerificationState("confirmed");
                        }, 500);
                        return prev;
                    }
                    return prev + 1;
                });
            }, 600);
            return () => clearInterval(interval);
        }
    }, [verificationState]);

    // Live countdown timer handler
    useEffect(() => {
        if (verificationState === "confirmed") {
            // Recalculate remaining seconds based on database created_at
            const calculateRemaining = () => {
                const createdTime = new Date(booking.created_at).getTime();
                const elapsedMs = Date.now() - createdTime;
                const remainingMs = (15 * 60 * 1000) - elapsedMs;
                return Math.max(0, Math.floor(remainingMs / 1000));
            };

            setCountdownSeconds(calculateRemaining());

            const timer = setInterval(() => {
                const rem = calculateRemaining();
                setCountdownSeconds(rem);
                if (rem <= 0) {
                    clearInterval(timer);
                }
            }, 1000);

            return () => clearInterval(timer);
        }
    }, [verificationState, booking.created_at]);

    // Handle cancellation
    const handleCancelBooking = async () => {
        setIsCancelling(true);
        try {
            await api.cancelBooking(booking.id);
            setVerificationState("cancelled");
            toast.success("Booking cancelled & refund initiated!");
        } catch (error) {
            toast.error((error as Error).message || "Failed to cancel booking");
        } finally {
            setIsCancelling(false);
        }
    };

    // Copy Booking ID
    const handleCopyId = () => {
        navigator.clipboard.writeText(booking.id);
        toast.success("Booking ID copied!");
    };


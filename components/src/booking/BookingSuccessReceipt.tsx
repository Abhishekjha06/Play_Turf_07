                                        rel="noopener noreferrer"
                                        className="pressable inline-flex items-center justify-center gap-2 rounded-2xl px-4 py-3 text-xs font-black uppercase tracking-wider transition cursor-pointer border border-border bg-panel text-foreground flex-1 text-center"
